export const t = (key, params) => {
  const keys = key.split('.');
  let obj = messages[locale.value];
  for (const k of keys) {
    if (!obj || obj[k] === undefined) return key;
    obj = obj[k];
  }
  if (typeof obj !== 'string') return key;
  if (params) {
    return obj.replace(/\{(\w+)\}/g, (_, k) => (params[k] !== undefined ? params[k] : `{${k}}`));
  }
  return obj;
};
